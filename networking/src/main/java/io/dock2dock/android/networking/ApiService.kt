object ApiService {
    private const val TIMEOUT_SECONDS = 15L

    inline fun <reified T> getRetrofitClient(): T {
        val config = Dock2DockConfiguration.instance()

        return Retrofit.Builder()
            .baseUrl(config.apiUrl)
            .addConverterFactory(GsonConverterFactory.create())
            .addCallAdapterFactory(ApiResponseCallAdapterFactory.create())
            .client(okHttpClientFactory(config.apiKey))
            .build()
            .create(T::class.java)
    }

    fun okHttpClientFactory(apiKey: String): OkHttpClient {
        val androidNorEarlier = Build.VERSION.SDK_INT <= 25

        val builder = OkHttpClient().newBuilder()
            .readTimeout(TIMEOUT_SECONDS, TimeUnit.SECONDS)
            .writeTimeout(TIMEOUT_SECONDS, TimeUnit.SECONDS)
            .addInterceptor { chain ->
                chain.request().newBuilder()
                    .addHeader("x-api-key", apiKey)
                    .build()
                    .let(chain::proceed)
            }

        if (androidNorEarlier) {
            val certificates = addHttpCertificatesForOlderAndroid()
            builder.sslSocketFactory(certificates.sslSocketFactory(), certificates.trustManager)
        }

        return builder.build()
    }

